package com.zhen.dagger211demo.bean;

public class Person {

    private String name = "小明";
    private int age = 15;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
